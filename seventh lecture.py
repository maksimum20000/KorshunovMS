#Задача 5
#Дан список: animals = ["собака", "кошка", "попугай", "хомяк"]
#Используя цикл for, выведи каждый элемент списка в формате: "Любимец: <животное>".
#Например, для первого элемента должно быть: Любимец: собака
animals = ["собака", "кошка", "попугай", "хомяк"]
