#Задача 3
#У тебя есть список: colors = ["красный", "зеленый", "синий", "желтый"]
#Используя цикл for, выведи все элементы списка, но останови цикл, как только встретится элемент "синий"

colors = ["красный", "зеленый", "синий", "желтый"]
for color in colors :

    if color == "синий":
        break
    print(color)

